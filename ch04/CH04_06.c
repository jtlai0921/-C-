#include<stdio.h>
#include<stdlib.h>

int main()
{
	int a=19,b=13; /* 宣告兩個整數變數*/ 
	/*比較運算子運算關係*/
    printf("a=%d b=%d \n",a,b);
    printf("--------------------------------\n");
    printf("a>b,比較結果為 %d 值\n",a>b);
    printf("a<b,比較結果為 %d 值\n",a<b);
    printf("a>=b,比較結果為 %d 值\n",a>=b);
    printf("a<=b,比較結果為 %d 值\n",a<=b);
    printf("a==b,比較結果為 %d 值\n",a==b);
    printf("a!=b,比較結果為 %d 值\n",a!=b);
    
    system("pause");    
    return 0;
 }
